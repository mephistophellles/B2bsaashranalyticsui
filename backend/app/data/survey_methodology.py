def seed_methodology_questions(db: Session) -> None:
    """Вставить 25 вопросов методики (идемпотентно: очищает таблицу вопросов)."""
    db.query(SurveyQuestion).delete()
    for block_index, order_in_block, text in METHODOLOGY_LIKERT_QUESTIONS:
        db.add(
            SurveyQuestion(
                block_index=block_index,
                order_in_block=order_in_block,
                text=text,
            )
        )
    db.commit()


def ensure_methodology_questions(db: Session) -> None:
    """Если в БД нет полного набора из 25 вопросов — пересоздать шаблон по методике."""
    n = db.query(SurveyQuestion).count()
    if n >= 25:
        return
    seed_methodology_questions(db)
